<h1>Solid state</h1>
<hr />
<p>In solid state [[storage]] devices, [[binary]] data is stored with the presence or absence of an electrical charge, meaning there are no moving parts as there are in other storage devices.</p>
<hr />
<p>As a result, solid state devices are <strong>much faster</strong> and <strong>more robust</strong> than their [[magnetic]] counterparts, and are generally <strong>more expensive</strong>.</p>
