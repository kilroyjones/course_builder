<h1>Serial data access</h1>
<p>In <strong>serial</strong> data [[storage]] devices, data is stored in <strong>sequential</strong> order.</p>
<p>![[sequentialdatastorage.png]]</p>
<p>This approach can be highly effective for use in [[backup]] strategies, but is less useful when files need to be accessed randomly as it is in [[direct]] storage devices.</p>
<p>[[Magnetic tape]] is the <em>only</em> example of a serial data storage device in the syllabus.</p>